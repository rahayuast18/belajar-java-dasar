public class ForLoop {
  public static void main(String[] args) {

    var num = 1;
    for (; num <=5; ) {
      System.out.println("Num " + num);
      num++;
    }

    for(var ber = 1; ber <= 7; ) {
      System.out.println("Number " + ber);
      ber++;
    }

    for (var counter = 1; counter <= 10; counter++) {
      System.out.println("Perulangan " + counter);
    }
  }
}
